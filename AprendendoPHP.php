Variáveis

-A representação de variável é feita por um "$" seguido pelo nome da variável

	-É Case Sensitive
	-Nomes de variável deve ser iniciado com letra ou sublinhado"_". Números podem aparecer em outras posições, exceto na primeira
	-Não pode ser usado caracteres especiais nas variáveis, nem acento.

Exemplos de variáveis

<?php

$nome = 'Mauro George';
$nascimento = '06/09/1989';
$sobre_nome = 'Oliveira Tavares';

?>

-O ";" é o separador de instruções

