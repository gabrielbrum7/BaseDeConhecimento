package my.tests;

public class Aritmeticos {
	
	public static void main (String[] args) {
		
		// Os operadores aritméticos, são: +, -, * e /. Sendo %, o resto da divisão inteira.
		
		int a = 2;
		int b = 2;
		
		int c = a + b;
		System.out.println("Soma: " + c);
		
		c = a - b;
		System.out.println("Subtração: " + c);
		a = 2;
		b = 2;
		
		c = a * b;
		System.out.println("Multiplicação: " + c);
		a = 2;
		b = 2;
		
		c = a / b;
		System.out.println("Divisão: " + c);
		
		a = 2;
		b = 2;
		
		c = a % b;
		System.out.println("Módulo: " + c);
		
		System.out.println("Exemplos aritméticos encerrados.");
	}
	
}
